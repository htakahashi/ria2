		<h2>Why you should Quilt:</h2>

			<p>There's one quilting technique that's sometimes regarded as a rule... pressing seam allowances to the side, rather than open, to help 'strengthen' the quilt. But is pressing to the side always a must? Many quilters don't think so.

			There are no rules in quilting -- other than standard safety concerns such as rotary cutting safety guidelines. Pressing depends on two things -- personal opinion and what's best for the current project.</p>
			<p>If you decide to work with seams that are pressed open, definitely stab a pin through intersections to be sure that seams are matched for sewing. Without loft as a guide, it's a little more difficult to know for certain that patches are aligned correctly.

			No matter which method you decide to use, remember to press, not iron, to avoid stretching the patchwork. Move the iron carefully and let its heat and weight do most of the work. Take special care not to stretch when using steam to press.</p>

